public class Switch{
    public static void main(String[] args){
        int a = 3;
        switch(a){
            case 1 : System.out.println("Hello Atef!");
                    break;
            case 2 : System.out.println("Hello Hafsa!");
                    break;
            case 3 : System.out.println("Hello Nowrin!");
                    break;
            default : System.out.println("Excuse me! Who are you?");
        }
    }
}