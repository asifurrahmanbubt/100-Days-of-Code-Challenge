public class Relational{
    public static void main(String[] args){
        int a = 10;
        int b = 20;
        System.out.println("is " + a + " Greater Than " + b + "? ans: " + (a > b));
        System.out.println("is " + a + " less Than " + b + "? ans: " + (a < b));
        System.out.println("is " + a + " equal " + b + "? ans: " + (a == b));
    }
}